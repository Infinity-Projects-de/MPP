    private Spell currentSpell;

    public MagicWand(ItemStack itemStack)
    {
        this.itemStack = itemStack;
        currentSpell = Spell.fromTag(itemStack);
        if (currentSpell == null)
        {
            currentSpell = Spell.LLAMA_SPELL;
            writeSpellNBT();
        }
    }

    public void nextSpell()
    {
        currentSpell = currentSpell.next();
        writeSpellNBT();
    }

    private void writeSpellNBT()
    {
        itemStack = UtilKt.setNBTString(itemStack, SELECTED_SPELL_TAG, currentSpell.name());
    }

    public void fire(Player player)
    {
        int range = currentSpell.getRange();
        double objectRange = Integer.MAX_VALUE;
        Object nearestObject = getNearestObject(player, range);
        Location location = player.getEyeLocation();

        if (nearestObject instanceof LivingEntity entity)
        {
            objectRange = location.distance(entity.getLocation());
            hitEntity(player, entity);
        }
        else if (nearestObject instanceof Block block)
        {
            objectRange = location.distance(block.getLocation());
            hitBlock(block);
        }

        fireParticleBeam(location, Math.min(objectRange, 20), currentSpell.getColor());
    }

    private void hitEntity(Player shooter, LivingEntity entity)
    {
        switch (currentSpell)
        {
            case SOUND_BEAM -> {
                if (entity instanceof Player target)
                {
                    Random random = new Random();
                    target.playSound(target.getEyeLocation(), soundBeamSounds[random.nextInt(soundBeamSounds.length)],
                            1f, random.nextFloat(0.85f, 1.15f));
                }
            }
            case YEET_SPELL -> {
                if (entity instanceof Player target)
                {
                    Vector velocity = shooter.getLocation().getDirection().add(new Vector(0, 2, 0));
                    target.setVelocity(entity.getVelocity().add(velocity));
                }
            }
        }
    }

    private void hitBlock(Block block)
    {
        if (currentSpell != Spell.LLAMA_SPELL) return;

        World world = block.getWorld();
        Location spawnLocation = block.getLocation();

        Llama llama = (Llama) world.spawnEntity(spawnLocation, EntityType.LLAMA);
        llama.setInvulnerable(true);
        llama.setVelocity(llama.getVelocity().add(new Vector(0, 1, 0)));

        new BukkitRunnable()
        {
            int counter = 0;

            @Override
            public void run()
            {
                if (llama.isOnGround() || counter >= 100)
                {
                    world.spawnParticle(Particle.EXPLOSION_HUGE, llama.getLocation(), 0);
                    world.playSound(llama.getLocation(), Sound.ENTITY_FIREWORK_ROCKET_BLAST, 1, 1);
                    this.cancel();
                    Bukkit.getScheduler().runTaskLater(Aether.getInstance(), llama::remove, 5L);
                    return;
                }

                counter += 5;
            }
        }.runTaskTimer(Aether.getInstance(), 20L, 5L);
    }

    /**
     * @return nearest object (block or entity) in sight of player or null if none was found
     */
    @Nullable
    private Object getNearestObject(Player player, int range)
    {
        Location location = player.getEyeLocation();

        Block block = getNearestBlockInSight(player, range);
        Entity entity = getNearestLivingEntityInSight(player, range);

        double blockRange = block != null ? location.distance(block.getLocation()) : Integer.MAX_VALUE;
        double entityRange = entity != null ? location.distance(entity.getLocation()) : Integer.MAX_VALUE;

        if (entity == null && block == null) { return null; }
        return blockRange < entityRange ? block : entity;
    }

    @Nullable
    private Block getNearestBlockInSight(Player player, int range)
    {
        return player.getTargetBlockExact(range, FluidCollisionMode.NEVER);
    }

    @Nullable
    private Entity getNearestLivingEntityInSight(Player player, int range)
    {
        Location location = player.getEyeLocation();
        Vector direction = location.getDirection();
        RayTraceResult result = player.getWorld().rayTraceEntities(location.add(direction), direction, range, 0);
        if (result == null) return null;

        Entity entity = result.getHitEntity();
        return entity instanceof LivingEntity && !entity.equals(player) ? entity : null;
    }

    private void fireParticleBeam(Location location, double range, Color color)
    {
        Location currentLocation = location.clone();
        Vector direction = location.getDirection();
        World world = location.getWorld();
        Particle.DustOptions dustOptions = new Particle.DustOptions(color, 1);
        while (location.distance(currentLocation) <= range)
        {
            world.spawnParticle(Particle.REDSTONE, currentLocation, 0, dustOptions);
            currentLocation.add(direction);
        }
    }
